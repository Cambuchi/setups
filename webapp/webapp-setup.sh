# create package.json
npm init -y

# install webpack and webpack cli
npm install --save-dev webpack webpack-cli

# install css loaders
npm install --save-dev style-loader css-loader

# instal html webpack to auto create html files
npm install --save-dev html-webpack-plugin

# install webpack server for real time development
npm install --save-dev webpack-dev-server

# install babel
npm install --save-dev @babel/core

# install babel-loader for webpack integration
npm install --save-dev babel-loader

# install jest
npm install --save-dev jest

# install babel jest compatibility
npm install --save-dev @babel/preset-env babel-jest

# install ES6 proxies for mocking webpack imports
npm install --save-dev identity-obj-proxy